    <h3 style="padding-bottom: 0px;">WHY MAP DAMS</h3>
    <p>Growing demand for power continues to drive hundreds of large dam projects across Asia. Many rivers bisect multiple countries but there is little transparency between the different governments or with communities likely affected. This leads to potentially serious environmental and social impacts such as flooding, siltation, and blocked fish migration, many likely exacerbated by climate change.</p>
    <p>Information, however, remains fragmented with no regional database. Available data is typically out of date, riddled with gaps and data errors, or is proprietary. This is particularly challenging for planned dams, which are not visible through satellites. This limits the information for researchers and advocates, preventing timely and accurate research, priority setting for campaigns, and reducing public awareness of dam risks.</p>
    <p>Without accurate maps, it’s conceivable that dam planners are even unaware that communities live in the impact zone. Without coordination, the race to dam Asia’s rivers could have major regional and even global security consequences, potentially driving conflict between countries.</p>
