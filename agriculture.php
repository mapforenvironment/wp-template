<div class="content" style="padding-top: 0px; padding-bottom: 0px;">
  <div class="row wp2">
    <h3 style="padding-bottom: 0px;">HOW?</h3>
    <p>To effectively map industrial agriculture, we need a range of different datasets:</p>
    <ol style="font-size: 1.2em; color: #646464; font-weight: 200;">
      <li style="padding-bottom: 10px;"><b>Partnerships on the ground</b> - Working with partners on the ground, we identify high priority plantations to map. This may be plantations where there are indications of primary forest clearance, endangered species, or community grievances with the operator.</li>
      <li style="padding-bottom: 10px;"><b>Source satellite imagery</b>  - from providers such as Planet Labs, DigitalGlobe, as well as the free Landsat archive</li>
      <li style="padding-bottom: 10px;"><b>Mapathons</b>  - Once identified, tasks will be created in tasking manager, and a call is sent out to the Map for Environment community to map theses areas. We will also organize mapathons with our partners in country.</li>
      <li style="padding-bottom: 10px;"><b>Publish maps and disseminate</b>  - Once we have mapped the plantations, maps will be made in MapHubs (<a href="https://maphubs.com">maphubs.com</a>) and available for anyone to use. They will disseminated to government regulatory agencies, advocacy groups as well as the companies involved.</li>
    </ol>
  </div>
</div>

<div class="content" style="padding-top: 0px; padding-bottom: 0px;">
