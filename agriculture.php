    <p>Effective mapping needs a combination of satellite imagery, plantation area maps, and field observations. Strong field to satellite monitoring can shine a spotlight on industrial agriculture operating in rainforest regions. </p>
    <p>Maps can define the boundaries of agriculture plantations, identifying overlapping land titles, and monitor illegal encroachment. Accurately mapped plantations can also improve the accuracy of forest alert systems such as Global Land Analysis and Discovery or GLAD alerts, which cannot distinguish between clearance of natural forest and former plantation. </p>
    <p>Carefully collected, mapping information provides a critical evidence for prosecuting environmental crimes and keeping supply chains deforestation free. Maps can also help compliant actors, whether smallholders or multinational, companies, prove that their supply chains do not result in deforestation. </p>
