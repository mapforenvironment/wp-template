    <h3 style="padding-bottom: 0px;">THE BENEFITS</h3>
    <p>There are many benefits to mapping industrial agriculture:</p>
    <ol style="font-size: 1.2em; color: #646464; font-weight: 200;">
      <li style="padding-bottom: 10px;"><b>Stronger monitoring</b> - monitors visiting plantations will have their own detailed maps of plantation sites, so they are not reliant on one’s provided by the companies.</li>
      <li style="padding-bottom: 10px;"><b>Improved forest alerts</b> - detailed plantation maps will improve the accuracy of deforestation alerts because we can mask out clearances of old plantation from natural forest clearance.</li>
      <li style="padding-bottom: 10px;"><b>Fines</b> - many lucrative markets such as the US, EU, and Australia have regulations on the importation of illegally sourced timber and oil palm. Accurate maps will add to evidence being used Justice departments bringing cases against violations.</li>
      <li style="padding-bottom: 10px;"><b>More accountability</b> - As companies become aware that they are being watched, this can lead to improved behavior, particularly if there is a stronger risk of fines or even closure by enforcement authorities.</li>
    </ol>
