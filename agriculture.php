    <p>Industrial agriculture is the single biggest cause of deforestation in Amazon and Indonesian rainforest. The expansion of oil palm, eucalyptus, soyabean, cacao, and host of other commodities have razed millions of hectares primary rainforest. In Indonesia, clearance has become so extensive that the International Union for Conservation of Nature has classified the orangutan as <a href="https://news.mongabay.com/2016/07/bornean-orangutan-declared-critically-endangered-as-forests-shrink/">critically endangered</a> - the highest risk extinction in the wild.</p>
