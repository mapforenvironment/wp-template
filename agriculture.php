    <p>The problem is greatly exacerbated by a lack of accurate maps. Plantation boundaries often change constantly or are not mapped at all, making establishing culpability for illegal clearance difficult. When monitors do visit, they rely upon maps provide by the company who also control access to plantations along private roads. While satellite images can help, cloud cover greatly reduces the availability of high resolution satellite images and remote products are no substitute for field-based monitors collecting information on the ground. </p>
