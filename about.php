    <h2>The goal</h2>
    <p>Map for Environment’s aim is to quite literally put logging roads, industrial agriculture, dams, and fracking on the map. The availability of high-resolution satellite information, open data portals such as Global Forest Watch (<a href="http://globalforestwatch.org" target="_blank">globalforestwatch.org</a>) and MapHubs (<a href="https://maphubs.com" target="_blank">maphubs.com</a>) has placed an unprecedented amount of environmental data in to public domain. OpenStreetMap (<a href="http://openstreetmap.org" target="_blank">openstreetmap.org</a>) – the crowdsourced map of the world – has also built a global community of volunteers mapping the planet.</p>
    <div style="width: 100%; text-align: center; padding: 25px 0px;">
      <img src="<?php bloginfo('template_directory'); ?>/img/about/sotmus2016.jpg" style="max-width: 100%; height: auto; margin: auto; text-align:center;" >
    </div>
    <p>The OSM community has pioneered crowd sourced mapping to support humanitarian organizations responding to disasters such as the Nepal earthquake and the 2013 Typhoon Yolanda/Haiyan in the Philippines. Map for Environment follows this model to map the unmapped environmmental disaster slowly unraveling in the world’s critical ecosystems.</p>
  </div>
