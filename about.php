    <h2>How it works</h2>
    <p>Map for Environment combines OpenStreetMap mapping tools with satellite imagery and location data of known logging, industrial agriculture, dam, and fracking locations to create detailed maps of infrastructure, size, as well as communities impacted. The information is mapped by OpenStreetMap users. Mapped data is shared on OpenStreetMap and will also available on MapHubs – a simple-to-use interactive map publishing platform.</p>
    <div style="width: 100%; text-align: center; padding: 25px 0px;">
      <img src="<?php bloginfo('template_directory'); ?>/img/about/MappingPartyKinshasa.jpg" style="max-width: 100%; height: auto; margin: auto; text-align:center;" >
    </div>
    <p>The Map for Environment team uses both open data and expert advice to direct efforts to maps dams, logging roads, industrial agriculture plantations, and fracking sites operating in high risk areas. This is ensures that we make high impact maps that directly support advocates and agencies holding operators accountable for illegal, unsustainable, and dangerous practices.</p>
  </div>
</div>

